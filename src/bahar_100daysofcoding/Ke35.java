package bahar_100daysofcoding;
public class Ke35 {
    public static void main(String[] args) {
    // Mendefinisikan variabel namaDepan dan namaBelakang
       String namaDepan = "Muhammad"; 
       String namaBelakang = "Bahar"; 

       System.out.println("Nama lengkap: " + namaDepan + " " + namaBelakang);

    // Mengubah nama depan menjadi "Abdul"
       namaDepan = "Abdul";

    // Menampilkan nama lengkap setelah mengubah nama depan
       System.out.println("Nama lengkap setelah diubah: " + namaDepan + " " + namaBelakang);
    }
}
